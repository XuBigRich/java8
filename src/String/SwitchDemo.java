package String;

public class SwitchDemo {
    public static void ss(String swit){

        switch (swit){
            case "1":
                System.out.println("输入的是:1");
                break;
            case "2":
                System.out.println("输入的是:2");
        }
    }
    public static void main(String[] args) {
    		SwitchDemo.ss("1");
    	}
}
